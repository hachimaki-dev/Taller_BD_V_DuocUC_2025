-- Tabla para almacenar usuarios del sistema con datos de ubicacion
CREATE TABLE USUARIOS (
    ID_USUARIO NUMBER NOT NULL,
    NOMBRE_USUARIO VARCHAR2(50) NOT NULL,
    EMAIL VARCHAR2(100) NOT NULL,
    PASSWORD_HASH VARCHAR2(255) NOT NULL,
    CIUDAD VARCHAR2(100) NOT NULL,
    PAIS VARCHAR2(100) NOT NULL,
    FECHA_REGISTRO DATE NOT NULL,
    CONSTRAINT PK_USUARIOS PRIMARY KEY (ID_USUARIO)
);

-- Tabla para almacenar el directorio de tiendas por ubicacion
CREATE TABLE TIENDAS (
    ID_TIENDA NUMBER NOT NULL,
    NOMBRE_TIENDA VARCHAR2(100) NOT NULL,
    DIRECCION VARCHAR2(200) NOT NULL,
    CIUDAD VARCHAR2(100) NOT NULL,
    PAIS VARCHAR2(100) NOT NULL,
    CONSTRAINT PK_TIENDAS PRIMARY KEY (ID_TIENDA)
);

-- Tabla para gestionar las tiendas favoritas de cada usuario
CREATE TABLE TIENDAS_FAVORITAS (
    ID_USUARIO NUMBER NOT NULL,
    ID_TIENDA NUMBER NOT NULL,
    FECHA_AGREGADO DATE NOT NULL,
    CONSTRAINT PK_TIENDAS_FAVORITAS PRIMARY KEY (ID_USUARIO, ID_TIENDA),
    CONSTRAINT FK_TIENDAS_FAV_USUARIO FOREIGN KEY (ID_USUARIO) REFERENCES USUARIOS(ID_USUARIO),
    CONSTRAINT FK_TIENDAS_FAV_TIENDA FOREIGN KEY (ID_TIENDA) REFERENCES TIENDAS(ID_TIENDA)
);

-- Tabla para almacenar los modelos de autos Hot Wheels
CREATE TABLE MODELOS_AUTOS (
    ID_MODELO NUMBER NOT NULL,
    NOMBRE_MODELO VARCHAR2(100) NOT NULL,
    ANIO NUMBER NOT NULL,
    LINEA_TEMA VARCHAR2(100) NOT NULL,
    RAREZA VARCHAR2(20) NOT NULL,
    URL_IMAGEN VARCHAR2(500),
    CONSTRAINT PK_MODELOS_AUTOS PRIMARY KEY (ID_MODELO),
    CONSTRAINT CK_RAREZA CHECK (RAREZA IN ('NINGUNO', 'TREASURE_HUNT', 'SUPER_TREASURE_HUNT'))
);

-- Tabla para almacenar las categorias de clasificacion de autos
CREATE TABLE CATEGORIAS (
    ID_CATEGORIA NUMBER NOT NULL,
    NOMBRE_CATEGORIA VARCHAR2(50) NOT NULL,
    DESCRIPCION VARCHAR2(200),
    CONSTRAINT PK_CATEGORIAS PRIMARY KEY (ID_CATEGORIA)
);

-- Tabla para relacionar modelos con sus categorias
CREATE TABLE MODELOS_CATEGORIAS (
    ID_MODELO NUMBER NOT NULL,
    ID_CATEGORIA NUMBER NOT NULL,
    CONSTRAINT PK_MODELOS_CATEGORIAS PRIMARY KEY (ID_MODELO, ID_CATEGORIA),
    CONSTRAINT FK_MOD_CAT_MODELO FOREIGN KEY (ID_MODELO) REFERENCES MODELOS_AUTOS(ID_MODELO),
    CONSTRAINT FK_MOD_CAT_CATEGORIA FOREIGN KEY (ID_CATEGORIA) REFERENCES CATEGORIAS(ID_CATEGORIA)
);

-- Tabla para almacenar la coleccion individual de cada usuario
CREATE TABLE COLECCION_USUARIOS (
    ID_COLECCION NUMBER NOT NULL,
    ID_USUARIO NUMBER NOT NULL,
    ID_MODELO NUMBER NOT NULL,
    ESTADO VARCHAR2(30) NOT NULL,
    FECHA_ADQUISICION DATE,
    CONSTRAINT PK_COLECCION_USUARIOS PRIMARY KEY (ID_COLECCION),
    CONSTRAINT FK_COLECCION_USUARIO FOREIGN KEY (ID_USUARIO) REFERENCES USUARIOS(ID_USUARIO),
    CONSTRAINT FK_COLECCION_MODELO FOREIGN KEY (ID_MODELO) REFERENCES MODELOS_AUTOS(ID_MODELO),
    CONSTRAINT CK_ESTADO CHECK (ESTADO IN ('NUEVO_CON_CAJA', 'NUEVO_SIN_CAJA', 'CON_DETALLES_CON_CAJA', 'CON_DETALLES_SIN_CAJA'))
);

-- Tabla para gestionar ofertas de venta y trueque entre coleccionistas
CREATE TABLE OFERTAS (
    ID_OFERTA NUMBER NOT NULL,
    ID_COLECCION NUMBER NOT NULL,
    TIPO_OFERTA VARCHAR2(20) NOT NULL,
    PRECIO NUMBER(10,2),
    AUTOS_DESEADOS VARCHAR2(500),
    ESTADO_OFERTA VARCHAR2(20) NOT NULL,
    FECHA_CREACION DATE NOT NULL,
    CONSTRAINT PK_OFERTAS PRIMARY KEY (ID_OFERTA),
    CONSTRAINT FK_OFERTAS_COLECCION FOREIGN KEY (ID_COLECCION) REFERENCES COLECCION_USUARIOS(ID_COLECCION),
    CONSTRAINT CK_TIPO_OFERTA CHECK (TIPO_OFERTA IN ('VENTA', 'TRUEQUE', 'VENTA_TRUEQUE')),
    CONSTRAINT CK_ESTADO_OFERTA CHECK (ESTADO_OFERTA IN ('ACTIVA', 'PAUSADA', 'COMPLETADA', 'CANCELADA'))
);

